package modelos;

public class Marca {

	// Atributos

	private String nome;
	private boolean possuiProdutosCadastrados;

	// Getter e setter

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public boolean isPossuiProdutosCadastrados() {
		return possuiProdutosCadastrados;
	}

	public void setPossuiProdutosCadastrados(boolean possuiProdutosCadastrados) {
		this.possuiProdutosCadastrados = possuiProdutosCadastrados;
	}

}
